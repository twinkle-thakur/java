package twinklethkur;

public class Swaping {

	public static void main(String[] args) {
//by using 3rd variable
		int a=6;
		int b=8;
		System.out.println("before swap ");
		System.out.println("a= "+a);
		System.out.println("b= "+b);
		int temp=a;
		a=b;
		b=temp;
		System.out.println("after swap");
		System.out.println("a= "+a);
		System.out.println("b= "+b);
		
	}

}
